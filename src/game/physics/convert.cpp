#include "convert.h"
#include "body.h"
#include <Box2D/Dynamics/b2Body.h>
#include <cassert>

namespace game
{
namespace physics
{
	b2Vec2 world_vector( const Body& ref, const b2Vec2& local )
	{
		assert( ref.body() );
		return ref.body()->GetWorldVector( local );
	}

	b2Vec2 local_vector( const Body& ref, const b2Vec2& global )
	{
		assert( ref.body() );
		return ref.body()->GetLocalVector( global );
	}

	b2Vec2 world_point( const Body& ref, const b2Vec2& local )
	{
		assert( ref.body() );
		return ref.body()->GetWorldPoint( local );
	}

	b2Vec2 local_point( const Body& ref, const b2Vec2& global )
	{
		assert( ref.body() );
		return ref.body()->GetLocalPoint( global );
	}
}
}
